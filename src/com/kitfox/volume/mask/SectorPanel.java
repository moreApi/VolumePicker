    private void updateFromCube()
    {
        SectorMask mask = cube.getOctantMask();
        Vector3f center = mask.getCenter();
        setSliderValue(slider_centerX, center.x);
        setSliderValue(slider_centerY, center.y);
        setSliderValue(slider_centerZ, center.z);

        int m = mask.getMask();
        check_q000.setSelected((m & Q000) != 0);
        check_q001.setSelected((m & Q001) != 0);
        check_q010.setSelected((m & Q010) != 0);
        check_q011.setSelected((m & Q011) != 0);
        check_q100.setSelected((m & Q100) != 0);
        check_q101.setSelected((m & Q101) != 0);
        check_q110.setSelected((m & Q110) != 0);
        check_q111.setSelected((m & Q111) != 0);

    }

    private void setSliderValue(JSlider slider, float value)
    {
        int max = slider.getMaximum();
        int min = slider.getMinimum();
        int val = (int)(min + value * (max - min));
        slider.setValue(val);
    }

    private float normalValue(JSlider slider)
    {
        int val = slider.getValue();
        int min = slider.getMinimum();
        int max = slider.getMaximum();

        return (float)(val - min) / (max - min);
    }

    private void updateCenter()
    {
        if (cube == null)
        {
            return;
        }

        float x = normalValue(slider_centerX);
        float y = normalValue(slider_centerY);
        float z = normalValue(slider_centerZ);
        SectorMask mask = cube.getOctantMask();
        mask.setCenter(new Vector3f(x, y, z));
    }

    private void updateMask()
    {
        if (cube == null)
        {
            return;
        }

        int val = 0;
        if (check_q000.isSelected()) { val |= Q000; }
        if (check_q100.isSelected()) { val |= Q100; }
        if (check_q010.isSelected()) { val |= Q010; }
        if (check_q110.isSelected()) { val |= Q110; }
        if (check_q001.isSelected()) { val |= Q001; }
        if (check_q101.isSelected()) { val |= Q101; }
        if (check_q011.isSelected()) { val |= Q011; }
        if (check_q111.isSelected()) { val |= Q111; }

        SectorMask mask = cube.getOctantMask();
        mask.setMask(val);
    }

    private void setSelection(int mask)
    {
        check_q111.setSelected((mask & Q111) != 0);
        check_q011.setSelected((mask & Q011) != 0);
        check_q101.setSelected((mask & Q101) != 0);
        check_q001.setSelected((mask & Q001) != 0);
        check_q110.setSelected((mask & Q110) != 0);
        check_q010.setSelected((mask & Q010) != 0);
        check_q100.setSelected((mask & Q100) != 0);
        check_q000.setSelected((mask & Q000) != 0);
    }


    public ViewerCube getCube()
    {
        return cube;
    }

    /**
     * Set the value of cube
     *
     * @param cube new value of cube
     */
    public void setCube(ViewerCube cube)
    {
        ViewerCube oldCube = this.cube;
        if (this.cube != null)
        {
            this.cube.getOctantMask().removePropertyChangeListener(this);
        }
        this.cube = cube;
        if (this.cube != null)
        {
            this.cube.getOctantMask().addPropertyChangeListener(this);
        }
        updateFromCube();
        firePropertyChange(PROP_CUBE, oldCube, cube);
    }

